#define PI fm::PI
constexpr long double delta = PI / 2 / 1500;
constexpr long double invDelta = 1. / delta;
// Step = 1500
// Absolute error: 0.000086667338112385097228206055
// Step = 2000
// Absolute error: 0.000086667042732095194703875740
// Step = 3000
// Absolute error: 0.000086666993912977351925512604
// Step = 4000
// Absolute error: 0.000086666910623899704358037710
// Step = 10000
// Absolute error: 0.000086666955641058989042932459
long double mSin(float x) {
  int sign = 1;
  if (x < 0) {
    x = -x;
    sign = -1;
  }
  int piN = x * fm::INV_PI;
  x = x - piN * PI;
  if (x > fm::PIdiv2) {
    x = PI - x;
  }

  long double pos = x * invDelta;
  auto flr = floor(pos);
  int iflr = flr;
  int cel = iflr + 1;
  pos = pos - flr;
  auto ret = (1. - pos) * SinPremuteTable[iflr] + pos * SinPremuteTable[cel];
  // ret *= sign;
  if (sign < 0) {
    ret = -ret;
  }
  if (piN % 2) {
    return -ret;
  }
  return ret;
}

#include <random>
#include <vector>

#define R_NUM 5000000
std::vector<FMFLOAT> preMute() {

  std::vector<FMFLOAT> ret;
  std::random_device seed;      // 硬件生成随机数种子
  std::ranlux48 engine(seed()); // 利用种子生成随机数引擎
  std::uniform_real_distribution<FMFLOAT> dist(-1000, 1000);
  for (long long i = 0; i < R_NUM; ++i) {
    ret.push_back(dist(engine));
  }
  return ret;
}

#include <functional>

void Timebenchmark(std::function<double(double)> funca,
                   const std::string &funcaname, std::vector<double> &premute) {
  std::cout.precision(17);
  std::cout << std::fixed;
  auto beg = std::chrono::steady_clock::now();
  volatile double tmp;
  for (auto t : premute) {
    tmp = funca(t);
  }
  auto end = std::chrono::steady_clock::now();

  std::cout << funcaname << " costs ==> "
            << std::chrono::duration_cast<std::chrono::milliseconds>(end - beg)
                       .count() /
                   1000.
            << "s\n\n";
}

void precisionTest(std::function<double(double)> funca,
                   std::function<double(double)> funcB,
                   const std::string &funcbname, double min, double max,
                   double step) {
  std::cout.precision(17);
  long double totalCount = (max - min) / step;
  long double meanError = 0;
  long double maxError = 0;
  for (long double t = min; t < max; t += step) {
    long double tmp = std::abs(funca(t) - funcB(t));
    meanError += tmp;
    maxError = std::max(tmp, maxError);
  }
  std::cout << funcbname << " Error test:\n"
            << "Mean Error: " << meanError / totalCount
            << "\nMax Error bounding: " << maxError << "\n\n";
}



